// Rotary encoder to f17/f18 for software remapping with Karabiner Elements 
bool encoder_update_user(uint8_t index, bool clockwise) {
    if (clockwise) {
      tap_code(KC_F18);
    } else {
      tap_code(KC_F17);
    }
    return true;
}

